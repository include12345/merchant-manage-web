<template>
  <div class="login-container">
    <el-form autoComplete="on" :model="loginForm" :rules="loginRules" ref="loginForm" label-position="left"
             label-width="0px" class="card-box login-form">
      <h3 class="title">商户会员管理系统</h3>
      <el-form-item prop="username">
        <el-input name="username" type="text" v-model="loginForm.username" autoComplete="on" placeholder="账号"/>
      </el-form-item>
      <el-form-item prop="password">
        <el-input name="password" type="password" @keyup.enter.native="handleLogin" v-model="loginForm.password"
                  autoComplete="on" placeholder="密码"/>
      </el-form-item>
      <el-row>
          <el-button type="primary" style="width:40%;" :loading="loading" @click.native.prevent="handleLogin">登录</el-button>
          <el-button type="primary" style="width:40%;margin-left: 60px" @click="toReg">注册</el-button>
      </el-row>
    </el-form>
  </div>
</template>

<script>
export default {
  name: 'login',
  data() {
    const validateUsername = (rule, value, callback) => {
      if (!value) {
        callback(new Error('用户名不能为空'))
      } else {
        callback()
      }
    }
    const validatePass = (rule, value, callback) => {
      if (!value) {
        callback(new Error('密码不能为空'))
      } else {
        callback()
      }
    }
    return {
      dialogVisible: false,
      loginForm: {
        username: this.username,
        password: this.password
      },
      loginRules: {
        username: [{
          required: true,
          trigger: 'blur',
          validator: validateUsername
        }],
        password: [{
          required: true,
          trigger: 'blur',
          validator: validatePass
        }]
      },
      loading: false
    }
  },
  methods: {
    handleLogin() {
      this.$refs.loginForm.validate(valid => {
        this.loading = true
        this.$store.dispatch('Login', this.loginForm).then(() => {
          this.loading = false
          this.$router.push({
            path: '/admin'
          })
        }).catch(() => {
          this.loading = false
        })
      })
    },
    toReg(){
     this.$router.push({path:'/reg'})
    }
  }
}
</script>

